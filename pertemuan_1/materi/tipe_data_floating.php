<?php 
// Bilangan pecahan (floating point)
$float1 = 1.234;
$float2 = 1.2e3; // 1.2 * 10^3
$float3 = 7E-10; // 7 * 10^-10
// Bilangan pecahan dengan underscore (garis bawah) untuk kemuda
$readableFloat = 1_234.567_890;
echo "Float 1: $float1\n"; // Output: Float 1: 1.234
echo "Float 2: $float2\n"; // Output: Float 2: 1200
echo "Float 3: $float3\n"; // Output: Float 3: 7.0E-10
echo "Readable Float: $readableFloat\n"; // Output: Readable Flo
?>