<?php
// Bilangan bulat decimal
$decimal = 1234;
// Bilangan bulat hexadecimal
$hexadecimal = 0x1A;
// Bilangan bulat octal
$octal = 0123;
// Bilangan bulat binary
$binary = 0b11111111;
// Bilangan dengan underscore (garis bawah) untuk kemudahan baca
$readableNumber = 1_000_000;
// Bilangan negatif
$negative = -1234;
echo "Decimal: $decimal\n"; // Output: Decimal: 1234
echo "Hexadecimal: $hexadecimal\n"; // Output: Hexadecimal: 26
echo "Octal: $octal\n"; // Output: Octal: 83
echo "Binary: $binary\n"; // Output: Binary: 255
echo "Readable Number: $readableNumber\n"; // Output: Readable N
echo "Negative: $negative\n"; // Output: Negative: -1234
?>